# Os operadores de comparação em Python são usados para comparar dois valores.
# Eles retornam um valor booleano (True ou False) com base na comparação realizada.
# Aqui estão os principais operadores de comparação em Python:

# Igualdade: ==
a = 5
b = 10
print("Igualdade (5 == 10):", a == b)  # False

# Desigualdade: !=
print("Desigualdade (5 != 10):", a != b)  # True

# Maior que: >
print("Maior que (5 > 10):", a > b)  # False

# Menor que: <
print("Menor que (5 < 10):", a < b)  # True

# Maior ou igual a: >=
print("Maior ou igual a (5 >= 10):", a >= b)  # False

# Menor ou igual a: <=
print("Menor ou igual a (5 <= 10):", a <= b)  # True

# Exemplos adicionais
x = 15
y = 15
print("Igualdade (15 == 15):", x == y)  # True
print("Desigualdade (15 != 15):", x != y)  # False
print("Maior que (15 > 10):", x > 10)  # True
print("Menor que (15 < 20):", x < 20)  # True
print("Maior ou igual a (15 >= 15):", x >= 15)  # True
print("Menor ou igual a (15 <= 10):", x <= 10)  # False

# Esses operadores são amplamente utilizados em estruturas de controle, como condicionais e loops, para tomar decisões com base nas comparações realizadas.