# Os operadores de atribuição em Python são usados para atribuir valores a variáveis.
# Eles combinam a atribuição com outras operações, permitindo atualizar o valor de uma variável de forma concisa.
# Aqui estão os principais operadores de atribuição em Python:

# Atribuição simples: =
x = 10
print("Atribuição simples (x = 10):", x)  # 10

# Atribuição com adição: +=
x += 5  # Equivalente a x = x + 5
print("Atribuição com adição (x += 5):", x)  # 15

# Atribuição com subtração: -=
x -= 3  # Equivalente a x = x - 3
print("Atribuição com subtração (x -= 3):", x)  # 12

# Atribuição com multiplicação: *=
x *= 2  # Equivalente a x = x * 2
print("Atribuição com multiplicação (x *= 2):", x)  # 24

# Atribuição com divisão: /=
x /= 4  # Equivalente a x = x / 4
print("Atribuição com divisão (x /= 4):", x)  # 6.0

# Atribuição com divisão inteira: //=
x //= 2  # Equivalente a x = x // 2
print("Atribuição com divisão inteira (x //= 2):", x)  # 3.0

# Atribuição com resto da divisão: %=
x %= 2  # Equivalente a x = x % 2
print("Atribuição com resto da divisão (x %= 2):", x)  # 1.0

# Atribuição com exponenciação: **=
x **= 3  # Equivalente a x = x ** 3
print("Atribuição com exponenciação (x **= 3):", x)  # 1.0

# Esses operadores de atribuição são úteis para simplificar o código e tornar as operações de atualização de variáveis mais legíveis.